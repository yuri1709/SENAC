public class calculadora {

	//método soma
	public int somar(int a, int b) {
		return a+b;
	}
	
	//método subtrair
	public int subtrair(int a, int b) {
		return a-b;
	}
	
	//método multiplicar
	public int multiplicar(int a, int b) {
		return a*b;
	}
	
	//método divisão de números inteiros
	public int divInt(int a, int b) {
		return a/b;
	}
	
}
